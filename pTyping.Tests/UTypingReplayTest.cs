                BeatmapId = "hash"
            };
            score.User.Username = "UTyping";
            foreach (ReplayFrame frame in Frames)
                score.ReplayFrames.Add(frame);

            Assert.Equal("UTyping", score.User.Username);
            Assert.Equal("hash",    score.BeatmapId);

            const string filePath = "utyping-save-test";

            score.SaveUTypingReplay(filePath);

            byte[] readBytes = File.ReadAllBytes(filePath);

            Assert.Equal(Arr.Length, readBytes.Length);
            for (int i = 0; i < Arr.Length; i++) {
                byte b = Arr[i];
                if (Math.Abs(b - (float)readBytes[i]) > 1)
                    Assert.Fail($"Read file at index {i} does not match! orig:{b} read:{readBytes[i]}");
            }
